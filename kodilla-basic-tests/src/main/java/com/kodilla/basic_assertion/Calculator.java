package com.kodilla.basic_assertion;

public class Calculator {

    public int sum(int a, int b){
        return a +b;
    }

    public int subtraction(int a, int b){
        return a -b;
    }
    public int powerOf2(int a){
        return a * a;
    }
    public int division(int a, int b){
        return b / a;
    }
}
